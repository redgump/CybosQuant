# -*- coding: utf-8 -*-
"""수집 설정.

값만 바꿔서 조회 주기·범위·저장 위치를 조정한다.
"""

# ---------------------------------------------------------------------------
# 분봉 주기 (분 단위). 1 = 1분봉, 3 = 3분봉, 5 = 5분봉 ...
# ---------------------------------------------------------------------------
CHART_PERIOD_MIN = 1

# ---------------------------------------------------------------------------
# 요청 방식
#   "count"  : 최근 COUNT 개의 봉을 요청 (간단, 기본값)
#   "period" : START_DATE ~ END_DATE 기간을 요청 (YYYYMMDD 문자열)
# ---------------------------------------------------------------------------
REQUEST_TYPE = "count"

# REQUEST_TYPE == "count" 일 때 종목당 받아올 최대 봉 개수.
# 연속조회로 이 개수에 도달하거나 더 이상 데이터가 없을 때까지 페이징한다.
#
# 1분봉은 CYBOS 가 2년치까지만 제공한다(공식 Q&A, 실측으로 확인).
# 매분 체결되는 대형주 기준 2년 = 약 190,000봉 이므로, 그보다 큰 값을 넣어
# "제공되는 전부"를 받게 한다. 실제로는 데이터가 끝나면 알아서 멈춘다.
COUNT = 200000

# REQUEST_TYPE == "period" 일 때 사용 (YYYYMMDD).
START_DATE = "20240101"
END_DATE = "20241231"

# 수정주가 반영 여부 (True: 액면분할 등 반영된 가격)
ADJUST_PRICE = True

# ---------------------------------------------------------------------------
# 조회 대상 시장
#   1 = 코스피(거래소), 2 = 코스닥
# ---------------------------------------------------------------------------
MARKETS = [1, 2]

# 보통주(주권)만 수집할지 여부. True 면 ETF/ETN/스팩/리츠 등 제외.
COMMON_STOCK_ONLY = True

# 테스트용: 앞에서 N개 종목만 처리하려면 양수로 지정 (0 = 전체)
LIMIT_STOCKS = 0

# ---------------------------------------------------------------------------
# 저장 위치
#   종목마다 data/{code}_{period}m.csv 파일로 저장한다.
# ---------------------------------------------------------------------------
OUTPUT_DIR = "data"

# 이미 CSV 파일이 있는 종목은 건너뛴다 (중단 후 재개용).
# False 면 기존 파일을 덮어쓴다.
SKIP_EXISTING = True

# ---------------------------------------------------------------------------
# 요청 제한 관리
#   CYBOS 는 시세요청이 15초당 60건으로 제한된다. 남은 요청 수가
#   이 값 이하로 떨어지면 제한이 풀릴 때까지 자동 대기한다.
# ---------------------------------------------------------------------------
REQUEST_MARGIN = 2

# ---------------------------------------------------------------------------
# 연결 끊김 가드
#   전체 수집은 10시간 이상 걸린다. 도중에 CYBOS 세션이 끊기면 이후 모든
#   조회가 실패하는데, 이를 종목별 실패로만 처리하면 남은 수천 종목을
#   순식간에 "실패"로 태우고 정상 종료한 것처럼 끝나버린다.
#   아래 설정으로 끊김을 감지해 대기/중단한다.
# ---------------------------------------------------------------------------

# 연결이 끊겼을 때 복구를 기다리는 최대 시간(초). 이 시간 안에 다시
# 연결되면 수집을 이어가고, 끝내 복구되지 않으면 중단한다.
# (CYBOS 재로그인은 사람이 해야 하므로 프로그램이 복구시킬 수는 없다.)
RECONNECT_WAIT_SEC = 600

# 연결 복구를 확인하는 간격(초).
RECONNECT_POLL_SEC = 30

# 연결은 정상인데 조회 실패만 연속으로 이어지는 경우의 중단 임계값.
# (거래정지 등 개별 종목 사유로 몇 건 실패하는 것은 정상이다.)
MAX_CONSECUTIVE_FAILURES = 30

# 실행 중 PC 절전 진입을 막을지 여부. 프로세스가 끝나면 자동 해제된다.
# (시스템 전원 설정 자체를 바꾸지 않는다.)
PREVENT_SLEEP = True
